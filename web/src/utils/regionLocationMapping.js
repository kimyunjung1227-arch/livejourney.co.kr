/**
 * 지역명으로 좌표 찾기
 * @param {string} locationName - 지역명
 * @returns {Object|null} { lat, lng } 또는 null
 */
export const getCoordinatesByLocation = (locationName) => {
  if (!locationName) return null;
  
  // 정확한 매칭 시도
  if (regionCoordinates[locationName]) {
    return regionCoordinates[locationName];
  }
  
  // 부분 매칭 시도 (예: "서울 명동" → "명동" 또는 "서울")
  for (const [key, coords] of Object.entries(regionCoordinates)) {
    if (locationName.includes(key) || key.includes(locationName)) {
      return coords;
    }
  }
  
  // 매칭 실패 시 null
  return null;
};

/**
 * 한글 초성 추출
 */
const getChosung = (str) => {
  const CHOSUNG = ['ㄱ', 'ㄲ', 'ㄴ', 'ㄷ', 'ㄸ', 'ㄹ', 'ㅁ', 'ㅂ', 'ㅃ', 'ㅅ', 'ㅆ', 'ㅇ', 'ㅈ', 'ㅉ', 'ㅊ', 'ㅋ', 'ㅌ', 'ㅍ', 'ㅎ'];
  let result = '';
  
  for (let i = 0; i < str.length; i++) {
    const code = str.charCodeAt(i) - 44032;
    if (code > -1 && code < 11172) {
      result += CHOSUNG[Math.floor(code / 588)];
    } else {
      result += str.charAt(i);
    }
  }
  return result;
};

/**
 * 지역명 검색 (자동완성용 - 초성 검색 지원)
 * @param {string} query - 검색어
 * @returns {Array} 매칭되는 지역 목록
 */
export const searchRegions = (query) => {
  if (!query) return [];
  
  const lowerQuery = query.toLowerCase();
  const queryChosung = getChosung(query);
  
  return Object.keys(regionCoordinates)
    .filter(region => {
      const lowerRegion = region.toLowerCase();
      const regionChosung = getChosung(region);
      
      // 1. 일반 텍스트 매칭
      const textMatch = region.includes(query) || lowerRegion.includes(lowerQuery);
      
      // 2. 초성 매칭
      const chosungMatch = regionChosung.includes(queryChosung) || regionChosung.startsWith(queryChosung);
      
      return textMatch || chosungMatch;
    })
    .slice(0, 20); // 최대 20개
};

export default {
  regionCoordinates,
  getCoordinatesByLocation,
  searchRegions
};













































const pick = (seed, list) => {
  const text = String(seed || 'seed');
  let hash = 0;
  for (let i = 0; i < text.length; i += 1) hash = (hash * 31 + text.charCodeAt(i)) >>> 0;
  return list[Math.abs(hash) % list.length];
};

